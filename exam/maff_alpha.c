#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	char a;
	int i;

	a = 'a';
	i = 1;
	while (i < 27)
	{
		if (i % 2 == 0)
		{
			a = a - 32;
			ft_putchar(a);
			a =  a + 33;
			i++;
		}
		else
		{
			ft_putchar(a);
			a++;
			i++;
		}
	}
	ft_putchar('\n');
	return (0);
}
