int		max(int *tab, unsigned int len)
{
  int i;
  int max_nb;

  max_nb = tab[0];
  i = 0;
  while (i < len)
  {
    if (tab[i] > max_nb)
    {
      max_nb = tab[i];
      i++;
    }
    i++;
  }
  return (max_nb);
}
